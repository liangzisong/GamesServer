package com.mangni.vegaplan.datatable;

public class TurnData {
	private String id,type,goodsid,jewelcharacter,chipcolor;
	private int probability=0,addturn=0;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getGoodsid() {
		return goodsid;
	}
	public void setGoodsid(String goodsid) {
		this.goodsid = goodsid;
	}
	public String getJewelcharacter() {
		return jewelcharacter;
	}
	public void setJewelcharacter(String jewelcharacter) {
		this.jewelcharacter = jewelcharacter;
	}
	public String getChipcolor() {
		return chipcolor;
	}
	public void setChipcolor(String chipcolor) {
		this.chipcolor = chipcolor;
	}
	public int getAddturn() {
		return addturn;
	}
	public void setAddturn(int addturn) {
		this.addturn = addturn;
	}
	public int getProbability() {
		return probability;
	}
	public void setProbability(int probability) {
		this.probability = probability;
	}
	
}
