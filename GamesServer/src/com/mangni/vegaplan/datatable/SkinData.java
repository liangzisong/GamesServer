package com.mangni.vegaplan.datatable;

public class SkinData {
	private String id,type,fighterid,desc;
	private int dayticketnum=0,weekticketnum=0,needstone=0,oldstone=0;

	public String getId() {
		return id;                                                                                                                
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getFighterid() {
		return fighterid;
	}

	public void setFighterid(String fighterid) {
		this.fighterid = fighterid;
	}

	public int getDayticketnum() {
		return dayticketnum;
	}

	public void setDayticketnum(int dayticketnum) {
		this.dayticketnum = dayticketnum;
	}

	public int getWeekticketnum() {
		return weekticketnum;
	}

	public void setWeekticketnum(int weekticketnum) {
		this.weekticketnum = weekticketnum;
	}

	public int getNeedstone() {
		return needstone;
	}

	public void setNeedstone(int needstone) {
		this.needstone = needstone;
	}

	public int getOldstone() {
		return oldstone;
	}

	public void setOldstone(int oldstone) {
		this.oldstone = oldstone;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
	
}
